package com.tetrapak.publicweb.core.beans;

/**
 * The Class GeneralInfoBean.
 */
public class GeneralInfoBean {

    /** The title. */
    private String title;

    /** The description. */
    private String description;

    /** The image. */
    private String image;

    /** The image alt text. */
    private String imageAltText;

    /** The link text. */
    private String linkText;

    /** The link path. */
    private String linkPath;

    /** The target blank. */
    private String targetBlank;

    /**
     * Gets the title.
     *
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * Sets the title.
     *
     * @param title
     *            the new title
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * Gets the description.
     *
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * Sets the description.
     *
     * @param description
     *            the new description
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * Gets the image.
     *
     * @return the image
     */
    public String getImage() {
        return image;
    }

    /**
     * Sets the image.
     *
     * @param image
     *            the new image
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     * Gets the image alt text.
     *
     * @return the image alt text
     */
    public String getImageAltText() {
        return imageAltText;
    }

    /**
     * Sets the image alt text.
     *
     * @param imageAltText
     *            the new image alt text
     */
    public void setImageAltText(String imageAltText) {
        this.imageAltText = imageAltText;
    }

    /**
     * Gets the link text.
     *
     * @return the link text
     */
    public String getLinkText() {
        return linkText;
    }

    /**
     * Sets the link text.
     *
     * @param linkText
     *            the new link text
     */
    public void setLinkText(String linkText) {
        this.linkText = linkText;
    }

    /**
     * Gets the link path.
     *
     * @return the link path
     */
    public String getLinkPath() {
        return linkPath;
    }

    /**
     * Sets the link path.
     *
     * @param linkPath
     *            the new link path
     */
    public void setLinkPath(String linkPath) {
        this.linkPath = linkPath;
    }

    /**
     * Gets the target blank.
     *
     * @return the target blank
     */
    public String getTargetBlank() {
        return targetBlank;
    }

    /**
     * Sets the target blank.
     *
     * @param targetBlank
     *            the new target blank
     */
    public void setTargetBlank(String targetBlank) {
        this.targetBlank = targetBlank;
    }

}
