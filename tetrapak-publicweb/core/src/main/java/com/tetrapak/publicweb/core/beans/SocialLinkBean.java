package com.tetrapak.publicweb.core.beans;

/**
 * The Class SocialLinkBean.
 */
public class SocialLinkBean {

    /** The social media link path. */
    private String socialMediaLinkPath;

    /** The social media icon class. */
    private String socialMediaIconClass;

    /** The social media name. */
    private String socialMediaName;

    /**
     * Gets the social media link path.
     *
     * @return the social media link path
     */
    public String getSocialMediaLinkPath() {
        return socialMediaLinkPath;
    }

    /**
     * Sets the social media link path.
     *
     * @param socialMediaLinkPath
     *            the new social media link path
     */
    public void setSocialMediaLinkPath(String socialMediaLinkPath) {
        this.socialMediaLinkPath = socialMediaLinkPath;
    }

    /**
     * Gets the social media icon class.
     *
     * @return the social media icon class
     */
    public String getSocialMediaIconClass() {
        return socialMediaIconClass;
    }

    /**
     * Sets the social media icon class.
     *
     * @param socialMediaIconClass
     *            the new social media icon class
     */
    public void setSocialMediaIconClass(String socialMediaIconClass) {
        this.socialMediaIconClass = socialMediaIconClass;
    }

    /**
     * Gets the social media name.
     *
     * @return the social media name
     */
    public String getSocialMediaName() {
        return socialMediaName;
    }

    /**
     * Sets the social media name.
     *
     * @param socialMediaName
     *            the new social media name
     */
    public void setSocialMediaName(String socialMediaName) {
        this.socialMediaName = socialMediaName;
    }

}
