package com.tetrapak.publicweb.core.beans;

/**
 * The Class OfficeBean.
 */
public class OfficeBean {

    /** The name. */
    private String name;

    /** The address. */
    private String address;

    /** The phone number. */
    private String phoneNumber;

    /** The fax. */
    private String fax;

    /** The longitude. */
    private Double longitude;

    /** The latitude. */
    private Double latitude;

    /** The local site url. */
    private String localSiteUrl;

    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name.
     *
     * @param name
     *            the new name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets the address.
     *
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * Sets the address.
     *
     * @param address
     *            the new address
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * Gets the phone number.
     *
     * @return the phone number
     */
    public String getPhoneNumber() {
        return phoneNumber;
    }

    /**
     * Sets the phone number.
     *
     * @param phoneNumber
     *            the new phone number
     */
    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    /**
     * Gets the fax.
     *
     * @return the fax
     */
    public String getFax() {
        return fax;
    }

    /**
     * Sets the fax.
     *
     * @param fax
     *            the new fax
     */
    public void setFax(String fax) {
        this.fax = fax;
    }

    /**
     * Gets the longitude.
     *
     * @return the longitude
     */
    public Double getLongitude() {
        return longitude;
    }

    /**
     * Sets the longitude.
     *
     * @param longitude
     *            the new longitude
     */
    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    /**
     * Gets the latitude.
     *
     * @return the latitude
     */
    public Double getLatitude() {
        return latitude;
    }

    /**
     * Sets the latitude.
     *
     * @param latitude
     *            the new latitude
     */
    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    /**
     * Gets the local site url.
     *
     * @return the local site url
     */
    public String getLocalSiteUrl() {
        return localSiteUrl;
    }

    /**
     * Sets the local site url.
     *
     * @param localSiteUrl
     *            the new local site url
     */
    public void setLocalSiteUrl(String localSiteUrl) {
        this.localSiteUrl = localSiteUrl;
    }
}
